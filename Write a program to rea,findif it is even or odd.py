n=int(input("Enter a number: "))
if n%3==0:
    print("The given number: ",n,"is even")
    print("The even number is:",n)
    print("The Square of the even number is:",n**2)
else:
    print("The given number",n,"is odd")
    print("The odd number is:",n)
    print("The cube of the odd number is",n**3)